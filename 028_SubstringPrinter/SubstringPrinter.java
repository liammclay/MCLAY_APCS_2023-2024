public class SubstringPrinter {
    public static void main(String[] args){
        String string = "The quick brown fox jumps over the lazy dog.";

        System.out.println(string.substring(40, 43));
        System.out.println(string.substring(16, 19));
        System.out.println(string.substring(37, 39));
        System.out.println(string.substring(17, 23));
        System.out.println(string.substring(42, 43));
    }
    
}
